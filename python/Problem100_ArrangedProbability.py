import math

blues = 5
while blues < 10**15:
    blues += 1
    v = blues * (blues -1)
    r = int(math.sqrt(v * 2))
    if r * (r+1) == v * 2:
        print(f"{blues}/{r+1} {blues-1}/{r} = 1/2    B={blues}  R={r+1-blues}  Total={r+1}")

